#include <stdio.h>
#include <stdlib.h>

int main()
{
     int i,n,max,min;
    printf("Program ini akan berhenti jika anda menginputkan angka  -99\n\n");
    scanf("%d",&n);
    max=n;
    min=n;
    for(i=1;i++;i++){if(n==-99)break;scanf("%d",&i);
    if (i==(-99) )break;max=((max>i)?max:i);min=((min<i)?min:i);}
    printf("\nmax=%d\n",max);
    printf("min=%d",min);
    return 0;
}
